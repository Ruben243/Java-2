/* Variables.java
 * Ejemplo uso de variables
 */
 
 public class Variables
 {
	 
	 public static void main (String args[])
	 {
		 int numero = 0;
		 String cadena = "Hola ....";
		 
		 System.out.printf ("El valor del numero es: %d", numero);
		 System.out.printf ("El valor de la cadena es: %s", cadena);
		 
		 numero = 10;
		 cadena = "Adios ...";
		 
		 System.out.printf ("El valor del numero es: %d", numero);
		 System.out.printf ("El valor de la cadena es: %s", cadena);
		 
		 
	}
 }
